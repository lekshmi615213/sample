function move(x) {
	var pen, looptimer, picture = document.getElementById("drawing-board");
	pen = picture.getContext("2d");
	pen.save();
	pen.clearRect(0, 0, 900,300);
	pen.fillStyle = "yellow";
	pen.beginPath();
	pen.arc(x+60,30,15,0,2*Math.PI);
	pen.fill();
	pen.closePath();
	pen.stroke();
	pen.fillStyle = "black";
	pen.beginPath();
	pen.arc(x+60, 30, 10, .8 * Math.PI, .2 * Math.PI, true);
	pen.fill();
	pen.closePath();
	pen.moveTo(x+53,25);
	pen.arc(x+53,25,1,0,2*Math.PI);
	pen.moveTo(x+67,25);
	pen.arc(x+67,25,1,0,2*Math.PI);
	pen.moveTo(x+60,45);
	pen.lineTo(x+60,100);
	pen.moveTo(x+60,60);
	pen.lineTo(x+40,70);
	pen.lineTo(x+20,60);
	pen.lineTo(x+20,30);
	pen.moveTo(x,30);
	pen.lineTo(x+40,30);
	pen.arc(x+20,30,20,Math.PI,2*Math.PI);
	pen.moveTo(x+60,60);
	pen.lineTo(x+90,65);
	pen.lineTo(x+60,80);
	pen.moveTo(x+60,100);
	pen.lineTo(x+30,140);
	pen.moveTo(x+60,100);
	pen.lineTo(x+90,140);
	pen.stroke();
	pen.restore();
	x += 5;
	looptimer = setTimeout('move('+x+')',1000);
}
